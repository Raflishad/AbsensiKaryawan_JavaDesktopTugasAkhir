    private void loadDashboardData() {
        try {
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/absensi", "root", "");
            // Hitung jumlah pegawai
            String sqlPegawai = "SELECT COUNT(*) FROM pegawai WHERE nip_pegawai LIKE 'P%'";
            PreparedStatement pstPegawai = conn.prepareStatement(sqlPegawai);
            ResultSet rsPegawai = pstPegawai.executeQuery();
            if (rsPegawai.next()) {
                jLabel2.setText(rsPegawai.getString(1));
            }

            // Hitung jumlah laporan (jika ada tabel laporan)
            String sqlLaporan = "SELECT COUNT(*) FROM rel_absensi"; // Ganti sesuai tabel yang ada
            PreparedStatement pstLaporan = conn.prepareStatement(sqlLaporan);
            ResultSet rsLaporan = pstLaporan.executeQuery();
            if (rsLaporan.next()) {
                jLabel3.setText(rsLaporan.getString(1));
            }

            // Hitung jumlah pegawai yang sudah absen hari ini
            String today = new SimpleDateFormat("yyyy-MM-dd").format(new Date());
            String sqlAbsen =   """
                                    SELECT COUNT(DISTINCT p.nip_pegawai) 
                                    FROM absensi a 
                                    JOIN rel_absensi ap ON a.id_absensi = ap.id_absensi
                                    JOIN pegawai p ON ap.nip_pegawai = p.nip_pegawai
                                    WHERE a.TANGGAL_ABSEN = ? AND p.nip_pegawai LIKE 'P%'
                                """;
            PreparedStatement pstAbsen = conn.prepareStatement(sqlAbsen);
            pstAbsen.setString(1, today);
             ResultSet rsAbsen = pstAbsen.executeQuery();
            if (rsAbsen.next()) {
                jLabel4.setText(rsAbsen.getString(1));
            }

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    public static void applyHoverEffect(Panel panel) {
    Color hoverColor = new Color(0, 102, 102);
    Color hoverTextColor = Color.WHITE; // Warna teks saat hover

    for (Component comp : panel.getComponents()) {
        if (comp instanceof Button) {
            Button button = (Button) comp;
            Color defaultColor = button.getBackground();
            Color defaultTextColor = button.getForeground(); // Simpan warna teks awal

            button.addMouseListener(new MouseAdapter() {
                @Override
                public void mouseEntered(MouseEvent e) {
                    button.setBackground(hoverColor);
                    button.setForeground(hoverTextColor); // Ubah warna teks jadi putih
                }

                @Override
                public void mouseExited(MouseEvent e) {
                    button.setBackground(defaultColor);
                    button.setForeground(defaultTextColor); // Kembalikan warna teks awal
                }
            });
        }
    }
}

